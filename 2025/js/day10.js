function combinationsExact(n, exactOnes) {
  const result = [];

  for (let i = 0; i < 1 << n; i++) {
    // Count ones
    let onesCount = 0;
    let temp = i;
    while (temp > 0) {
      onesCount += temp & 1;
      temp >>= 1;
    }

    // Only include if ones count == exactOnes
    if (onesCount === exactOnes) {
      const combo = [];
      for (let j = 0; j < n; j++) {
        combo.push((i >> j) & 1);
      }
      result.push(combo);
    }
  }

  return result;
}

